import type { RequestGameState } from "../types/game";

export function sceneFragmentPrompt(
  gameState: RequestGameState,
  sceneId: string,
  mode: "dialogue" | "fragment",
  storyContext: string,
  scenePrompt: string,
  requiredLines: string[],
  skeletonLines: string[] = []
) {
  const exampleScript = [
    "[旁白]示例旁白内容。",
    "",
    "[NPC:刘宇]示例NPC台词。",
    "",
    "[主角说]示例主角台词。",
    "",
    "[旁白]示例收束动作。",
  ].join("\\n");

  const hasRequired = requiredLines.length > 0;
  const hasSkeleton = skeletonLines.length > 0;
  const unconditionalLines: string[] = [];
  const conditionalLines: string[] = [];
  for (const line of requiredLines) {
    if (/^\[若/.test(line) || /^\[高/.test(line)) {
      conditionalLines.push(line);
    } else {
      unconditionalLines.push(line);
    }
  }

  const unconditionalBlock = unconditionalLines.join("\n  ");
  const conditionalBlock = conditionalLines.join("\n  ");

  // ── 骨架段落：必须展开（新增）──
  let skeletonSection = "";
  if (hasSkeleton) {
    const skeletonLinesBlock = skeletonLines.map((line, i) => `  [骨架${i + 1}] ${line}`).join("\n");
    skeletonSection = [
      "═══════════════════════════════════",
      "【必须展开的段落骨架 — 最高优先级，不可遗漏任何一个骨架单元】",
      "═══════════════════════════════════",
      "以下每一段都是场景中必须出现的核心内容，但你不可直接照抄——必须在保留其关键句子的前提下，在每个骨架句前后增加2-4句环境描写、情绪铺垫、心理活动或细节描写，使其成为完整丰满的叙事段落。",
      "",
      "展开规则：",
      "  · 每个骨架句中标记的核心句子必须原样保留，措辞和观点不得修改",
      "  · 每句前后必须扩展环境、动作、神态、心理等细节",
      "  · 骨架句之间的过渡必须自然流畅，保持叙述逻辑",
      "  · 骨架句出现的顺序必须与下方一致，不得打乱先后",
      "",
      skeletonLinesBlock,
      "",
    ].join("\n");
  }

  // 必说台词独立高亮区块
  let requiredLinesSection = "";
  if (unconditionalLines.length > 0) {
    requiredLinesSection += [
      "═══════════════════════════════════",
      "【必说台词（无条件）— 强制执行，不可遗漏任何一行】",
      "═══════════════════════════════════",
      "以下台词必须逐字逐句完整出现在最终script中，不得改动任何标点或字词：",
      "",
      `  ${unconditionalBlock}`,
      "",
    ].join("\n");
  }
  if (conditionalLines.length > 0) {
    requiredLinesSection += [
      "【条件必须台词 — 若当前分支匹配则必须逐字包含】",
      "根据上方「当前玩家选择」和动态分支条件，匹配到的行必须出现在script中：",
      "",
      `  ${conditionalBlock}`,
      "",
      "⚠ 匹配到的行也必须逐字逐句完整包含。未匹配到的行可忽略。",
      "",
    ].join("\n");
  }
  if (!hasRequired) {
    requiredLinesSection = "";
  }

  // ── 场景强制结构：骨架+必说台词同时存在时，必须定义4阶段生产顺序 ──
  let structureSection = "";
  if (hasSkeleton && hasRequired) {
    structureSection = [
      "═══════════════════════════════════",
      "【场景强制结构 — 绝对不可违反的生成顺序，此为最高优先级指令】",
      "═══════════════════════════════════",
      "本场景生成时必须严格遵循以下4阶段顺序，不得判断是否合理、不得自行调整先后、不得跳过任何阶段：",
      "",
      "  ┌─ 阶段1：开场旁白（展开全部骨架句中的前半部分） ─────────────┐",
      "  │ 以「必须展开的段落骨架」区块中的前半批骨架句为核心，逐句展开。  │",
      "  │ 每句前后增加2-4句环境描写、情绪铺垫或心理活动。                │",
      "  │ 这是社会现实层面的客观铺陈，为后续NPC的反应提供事实依据。      │",
      "  └──────────────────────────────────────────────────────┘",
      "                          ↓ 阶段1完成后才能进入阶段2",
      "  ┌─ 阶段2：NPC追问（逐字出现必说台词） ───────────────────┐",
      "  │ 紧接着阶段1的旁白结束之后，必须逐字出现「必说台词」区块中的  │",
      "  │ NPC对话行。这是NPC对阶段1内容的自然反应——追问原因。       │",
      "  └──────────────────────────────────────────────────────┘",
      "                          ↓ 阶段2完成后才能进入阶段3",
      "  ┌─ 阶段3：主角回应（自由创作，受动态分支约束） ───────────┐",
      "  │ 阶段2之后，根据「动态分支」规则生成主角的解释性回应。      │",
      "  │ 必须承接阶段2的追问，体现真诚而非说教。                   │",
      "  └──────────────────────────────────────────────────────┘",
      "                          ↓ 阶段3完成后才能进入阶段4",
      "  ┌─ 阶段4：收束旁白（展开全部骨架句中的后半部分） ─────────┐",
      "  │ 以「必须展开的段落骨架」区块中的后半批骨架句为核心，逐句展开。│",
      "  │ 从\"怔住\"→\"被尊重\"→\"理解\"→\"发现\"逐层递进，每句增加心理/神态细节。│",
      "  │ 最后让NPC产生疑问，为后续场景承接。                        │",
      "  └──────────────────────────────────────────────────────┘",
      "",
      "⚠ 以上阶段1→阶段2→阶段3→阶段4的顺序决不可调换、跳跃或合并。",
      "⚠ 骨架句不可在阶段1和阶段4之间穿插复用——前半部分与后半部分必须严格分开。",
      "⚠ 如果输出中缺少任一阶段，或者阶段顺序错乱，则视为违反最高优先级指令。",
      "",
    ].join("\n");
  }

  const choiceHistory = gameState.choiceHistory ?? [];
  const lastChoice = choiceHistory.length > 0 ? choiceHistory[choiceHistory.length - 1] : null;
  const recentChoices = choiceHistory.length > 0
    ? `最近一次选择ID：${lastChoice}\n完整选择历史：${JSON.stringify(choiceHistory)}`
    : "尚未做出任何选择。";

  return [
    // ── 身份与任务 ──────────────────────────────────────────
    "你是视觉小说《快乐小孩》的动态剧情生成模块，负责生成一小段剧本编码文本。",
    `当前场景ID：${sceneId}`,
    `生成模式：${mode === "fragment" ? "AI片段（多角色动态小场景）" : "AI对话（NPC主导）"}`,
    "",
    // ── 输出格式（最高优先级）──────────────────────────────
    "═══════════════════════════════════",
    "【输出格式 — 最高优先级，不可违反】",
    "═══════════════════════════════════",
    "你的完整回复必须是且只能是下面这种合法JSON对象，不得在JSON前后附加任何文字、标点或换行：",
    "",
    `{"script":"${exampleScript}"}`,
    "",
    "script字段规则：",
    "  · 只能使用以下角色码开头的段落：[旁白]  [主角]  [主角说]  [NPC:角色名]",
    "  · 每个段落之间用字面量 \\n\\n 分隔（即JSON字符串内两个反斜杠n）",
    "  · 禁止在JSON之外输出任何内容，包括markdown代码块、说明文字、道歉语句",
    "  · 禁止直接扮演角色说话——你输出的是JSON，不是对话",
    "  · 如果不确定格式，请参考上方示例结构",
    "",
    // ── 骨架段落（必须展开）────────────────────────────────
    skeletonSection,
    // ── 场景强制结构（骨架+必说台词同时存在时生效）────────────
    structureSection,
    // ── 必说台词（强制执行，与格式同级）─────────────────────
    requiredLinesSection,
    // ── 剧情规则 ────────────────────────────────────────────
    "剧情规则（在满足输出格式前提下遵守）：",
    "1. 若有「场景强制结构」区块：此为最高优先级指令——必须严格按阶段1→阶段2→阶段3→阶段4的顺序生成，不得自行调整先后、不得跳过、不得合并阶段。若没有该区块，则检查「必须展开的段落骨架」区块：必须按照骨架顺序逐一展开每个单元——保留骨架中的核心句子，并在前后增加2-4句环境/心理/神态/动作描写使其成为丰满的自然段，不得照抄不展开、不得删减核心句、不得打乱顺序。",
    "2. 若有「必说台词（无条件）」区块：必须逐字保留其中的全部台词，并检查「条件必须台词」区块中匹配当前分支的行，不得遗漏任何一行。",
    "3. 不得创造新的关键世界观事实，不得改变死亡、许可、线索、主线结果或下一场景。",
    "4. 场景要求中的分支条件（如「若选择XXX→……」）必须严格匹配下方「当前玩家选择」给出的选择ID。根据该选择对应的分支、七维人格、NPC信任和flags调整语气与反应。",
    "5. 片段必须有完整收束：NPC对主角的安慰/帮助/质问/交易必须至少回应一句，随后用[旁白]交代动作或情绪变化。不限制行数，必须保证对话逻辑完整闭环。",
    "6. 内容要求丰富充实：请尽可能展开细节——描写动作神态、环境氛围、心理活动，让每段对话都有来有回而非一口气说完；旁白应交代场景变化和情绪递进，避免干瘪的流水账式快速推进。",
    hasRequired ? "" : "7. 没有指定必说台词时，不要重复同一句话。",
    "",
    // ── 玩家选择（最高优先级，匹配分支条件的唯一依据）─────
    "═══════════════════════════════════",
    "【当前玩家选择 — 场景分支条件的匹配依据】",
    "═══════════════════════════════════",
    recentChoices,
    "",
    // ── 游戏状态 ────────────────────────────────────────────
    "玩家当前状态：",
    JSON.stringify(gameState, null, 2),
    "",
    // ── 世界观 ──────────────────────────────────────────────
    "全局世界观底座：",
    "《快乐小孩》是规则副本视觉小说。主角叶平生被困在学校/家庭等区域规则中，表层规则背后指向「好孩子」、服从、成绩、家庭期待和精神匮乏等主题。AI只能补写当前小场景的对话、旁白和简单动作，不能提前揭秘根本规则，不能改变主线结果，不能把NPC写成普通现实世界人物而忘记副本规则压力。",
    "",
    // ── 剧情上下文 ──────────────────────────────────────────
    "主线剧情上下文：",
    storyContext.trim() || "无额外上下文。仍需严格依据本场景剧本要求，不要脱离《快乐小孩》的学校/家庭规则副本语境。",
    "",
    // ── 本场景要求 ──────────────────────────────────────────
    "本场景剧本要求：",
    scenePrompt,
    "",
    // ── 输出格式再次强调（末尾锚定）────────────────────────
    "▶ 再次确认：现在请直接输出JSON对象，第一个字符必须是 { ，最后一个字符必须是 } ，中间只有script一个键。",
  ].join("\n");
}
